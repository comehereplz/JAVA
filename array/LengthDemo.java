package org.opentutorials.javatutorials.array;

public class LengthDemo {

	public static void main(String[] args) {
		String[] classGroup = new String[3];
		classGroup[0] = "오세연";
		System.out.println(classGroup.length);
		classGroup[1] = "박지원";
		System.out.println(classGroup.length);
		classGroup[2] = "김진주";
		System.out.println(classGroup.length);
	}

}
